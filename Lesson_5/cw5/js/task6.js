var left = 0;

function doCoffee(beans) {
    if (isNaN(beans)) {
        return `entered incorrect value`
    } else {
        if (beans % 5 == 0) {
            return `Prepered cups of coffee: ${beans / 5}, left beans: ${left += (beans % 5)}`
        } else if (beans < 5) {
            return `Add, please, more beans? \n Prepered cups of coffee: ${Math.floor(beans / 5)}, осталось зерен: ${left += (beans % 5)}`;
        } else if (beans % 5 != 0) {
            return `Prepered cups of coffee: ${Math.floor(beans / 5)
            }, left beans: ${
                left += (beans % 5)
            }`
        }
    }
}

console.log(doCoffee(10));//"Приготовлено чашек кофе: 2, осталось зерен: 0";
console.log(doCoffee(4)); // "Можно еще зерен?" => "Приготовлено чашек кофе: 1, осталось зерен: 2";
console.log(doCoffee(3));//"Приготовлено чашек кофе: 1, осталось зерен: 0";